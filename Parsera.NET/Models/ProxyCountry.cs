namespace Parsera.Models
{
    /// <summary>
    /// Represents a proxy country.
    /// </summary>
    public class ProxyCountry
    {
        /// <summary>
        /// Gets or sets the friendly name of the country.
        /// </summary>
        public string FriendlyName { get; set; }

        /// <summary>
        /// Gets or sets the system name of the country.
        /// </summary>
        public string SystemName { get; set; }
    }
}
